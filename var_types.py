# Описание студента
# str - строка
name_student = 'Max'
print(name_student,type(name_student))

# int - целое число
age_student = 20
print(age_student,type(age_student))

# float - число с плавающей точкой
avg_mark = 4.5
print(avg_mark,type(avg_mark))

# bool - логический тип
is_man = True
print(is_man,type(is_man))